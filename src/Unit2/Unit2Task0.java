package Unit2;

public class Unit2Task0 {
    public static void main(String[] args) {
        for(int i=0; i<=10; i++) {
            System.out.print(i+" ");
        }
        int i = 0;
        System.out.println();
        while(i<11) {
            System.out.print(i+" ");
            i++;
        }
        i=0;
        System.out.println();
        do {
            System.out.print(i+" ");
            i++;
        }while(i!=11);
    }
}
