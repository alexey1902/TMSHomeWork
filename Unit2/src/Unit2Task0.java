public class Unit2Task0 {
    public static void main(String[] args) {
        for(int i=1; i<11; i++) {
            System.out.print(i+" ");
        }
        int i = 1;
        System.out.println();
        while(i!=11) {
            System.out.print(i+" ");
            i++;
        }
        i=1;
        System.out.println();
        do {
            System.out.print(i+" ");
            i++;
        }while(i!=11);
    }
}
